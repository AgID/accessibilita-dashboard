      <h2 class="lead">Media Policy</h2>
      <p class="py-3">
        La media policy esterna illustra agli utenti le regole di comportamento
        da tenere negli spazi di presidio di AgID e indica quali contenuti e
        quali modalità di relazione ci si deve aspettare. L’Agenzia per l’Italia
        Digitale (di seguito Agenzia o AgID) utilizza il proprio sito web e i
        social media per informare gli utenti e comunicare con loro attraverso
        Internet. Il sito e i canali social sono gestiti dall’Ufficio
        Comunicazione e Relazioni esterne dell’Agenzia che può avvalersi di
        collaboratori e consulenti esterni.
      </p>
    </div>

    <div class="pb-3">
      <h4 class="h4 blueText">I nostri contenuti</h4>
      <p class="py-3">
        I contenuti pubblicati sul sito e sui canali social sono concepiti per
        informare PA, cittadini e imprese sulle attività, i progetti, le
        iniziative e gli eventi promossi di AgID, e per favorirne la diffusione.
        I canali social sono usati, inoltre, per la partecipazione attraverso il
        confronto e il dialogo nell’ottica della trasparenza e della
        condivisione. Lo staff realizza contenuti originali di carattere
        testuale, fotografico, infografico, video, etc… che sono utilizzabili
        con Licenza CC (Creative Commons - Attribuzione 3.0). Attraverso i
        canali dell’Agenzia potranno altresì essere condivisi contenuti e
        messaggi di pubblico interesse e utilità realizzati da soggetti terzi
        (Istituzioni, associazioni, imprese, media, cittadini) previa verifica
        dell’attendibilità della fonte.
        <br />
        <br />
        In relazione alle norme sull’accesso civico generalizzato (cd. FOIA), lo
        staff promuove e valorizza il dialogo collaborativo con le comunità di utenti
        dei social media (Facebook, Twitter, ecc.), secondo le indicazioni della
        <a
          title="Il link si apre in una nuova finestra"
          href="https://www.funzionepubblica.gov.it/articolo/dipartimento/01-06-2017/circolare-n-2-2017-attuazione-delle-norme-sull%E2%80%99accesso-civico"
          target="_blank"
          rel="noreferrer"
          >Circolare 2/2017 <Icon
            name="it it-external-link"
            variant="primary"
            size="xs"
            customClass="mb-1"
          />
        </a>
        del Dipartimento della funzione pubblica. La presenza di spazi pubblicitari
        a margine dei contenuti pubblicati nelle pagine dei social network utilizzati
        da AgID non è sotto il controllo dell’Agenzia, ma è gestita in autonomia
        dagli amministratori dei singoli social network.
        <br />
        <br />
        AgID non promuove e non condivide:
      </p>
      <ul>
        <li>
          <span>
            su tematiche oggetto di controversie legali o affermazioni che
            potrebbero essere utilizzate contro l’Agenzia;</span
          >
        </li>
        <li>
          <span>
            informazioni riservate o protette dal diritto alla privacy, come la
            corrispondenza interna, informazioni di terze parti su contenuti,
            documenti, progetti non ancora resi pubblici o relativi a
            procedimenti in corso;</span
          >
        </li>
        <li><span> informazioni personali o dati sensibili.</span></li>
      </ul>
    </div>
    <div class="pb-3">
      <h4 class="h4 blueText">Moderazione e netiquette</h4>
      <p class="py-3">
        I canali social di AgID vengono costantemente e sistematicamente
        moderati. AgID si impegna a gestire spazi di comunicazione e dialogo
        all’interno dei propri profili social chiedendo ai propri utenti il
        rispetto di alcune semplici regole di netiquette:
      </p>
      <ol>
        <li>
          <span>
            A tutti si chiede di esporre la propria opinione con correttezza e
            misura, basandosi per quanto possibile su dati di fatto verificabili
            e di rispettare le opinioni altrui.</span
          >
        </li>
        <li>
          <span
            >Nei social network ognuno è responsabile dei contenuti che pubblica
            e delle opinioni che esprime. Non saranno comunque tollerati
            insulti, volgarità, offese, minacce e, in generale, atteggiamenti
            violenti.</span
          >
        </li>
        <li>
          <span
            >I contenuti pubblicati devono rispettare sempre la privacy delle
            persone. Vanno evitati riferimenti a fatti o a dettagli privi di
            rilevanza pubblica e che ledano la sfera personale di terzi.</span
          >
        </li>
        <li>
          <span
            >Requisito essenziale degli argomenti sui canali social dell’Agenzia
            è l’interesse pubblico. Non è possibile utilizzare questi spazi per
            affrontare casi personali, ad eccezione del gruppo Facebook “SPID”
            dedicato alla discussione pubblica - in maniera rispettosa e
            propositiva - e all’assistenza degli utenti.</span
          >
        </li>
        <li>
          <span
            >L’interesse degli argomenti rispetto alle finalità di AgID è un
            requisito essenziale: non è possibile utilizzare questi spazi per
            scopi diversi da quelli istituzionali dell’Agenzia.</span
          >
        </li>
        <li>
          <span
            >Ogni discussione è legata a un tema specifico: chiediamo a tutti i
            partecipanti di rispettarlo, evitando di allargare il confronto in
            modo generico e indiscriminato.</span
          >
        </li>
        <li>
          <span
            >Non sarà tollerata alcuna forma di pubblicità, spam o promozione di
            interessi privati o di attività illegali.</span
          >
        </li>
        <li>
          <span
            >Non sono ammessi contenuti che violino il diritto d’autore né
            l’utilizzo non autorizzato di marchi registrati.</span
          >
        </li>
      </ol>
      <p>
        In ogni caso, saranno rimossi dallo staff tutti i post, i commenti o i
        materiali audio/video che:
      </p>
      <ol>
        <li><span>hanno un contenuto politico o propagandistico;</span></li>
        <li>
          <span
            >presentano un linguaggio inappropriato e/o un tono minaccioso,
            violento, volgare o irrispettoso;</span
          >
        </li>
        <li>
          <span
            >presentano contenuti illeciti o che incitano a compiere attività
            illecite;</span
          >
        </li>
        <li>
          <span
            >hanno contenuti offensivi, ingannevoli, allarmistici, o in
            violazione di diritti di terzi;</span
          >
        </li>
        <li>
          <span
            >divulgano dati e informazioni personali o che possono cagionare
            danni o ledere la reputazione di terzi;</span
          >
        </li>
        <li>
          <span
            >presentano contenuto osceno, pornografico o pedopornografico, o
            tale da offendere la sensibilità degli utenti;</span
          >
        </li>
        <li>
          <span
            >hanno un contenuto discriminatorio per genere, razza, etnia,
            lingua, credo religioso, opinioni politiche, orientamento sessuale,
            età, condizioni personali e sociali;</span
          >
        </li>
        <li>
          <span
            >promuovono o sostengono attività illegali, che violano il copyright
            o che utilizzano in modo improprio un marchio registrato.</span
          >
        </li>
      </ol>
      <p>
        Per chi dovesse violare ripetutamente queste condizioni o quelle
        contenute nelle policy degli strumenti adottati, lo staff si riserva il
        diritto di usare il ban o il blocco (quando possibile dopo un primo
        avvertimento) per impedire ulteriori interventi, e di segnalare l’utente
        ai responsabili della piattaforma ed eventualmente alle forze
        dell’ordine. Messaggi contenenti dati personali (indirizzi email, numeri
        di telefono, indirizzi, etc) potranno essere rimossi a tutela delle
        persone interessate. Per ogni approfondimento relativo al trattamento
        dei dati personali è possibile consultare la <a href="/privacy-policy"
          >Privacy policy</a
        >
        e le
        <a href="/note-legali">Note legali</a> dell’Agenzia.
      </p>
